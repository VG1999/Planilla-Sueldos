    private void RegistroEmpleadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistroEmpleadosActionPerformed

        if(txtEmpleado.getText().equals("")){

            JOptionPane.showMessageDialog(null,"Ingrese el Usuario");
        }
        if(txtNIT.getText().equals("")){

            JOptionPane.showMessageDialog(null,"Ingrese la Contraseña");
        }
        
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/empleados", "root", "");
            PreparedStatement pst = cn.prepareStatement("insert into empleado values(?,?,?,?,?,?,?)");

            pst.setString(1, "0");
            pst.setString(2, txtEmpleado.getText().trim());
            pst.setString(3, txtNIT.getText().trim());
            pst.setString(4, txtDireccion.getText().trim());
            pst.setString(5, txtSueldo.getText().trim());
            pst.setString(6, txtEstado.getText().trim());
            pst.setString(7, txtDepartamento.getText().trim());
                
            pst.executeUpdate();

            txtEmpleado.setText("");
            txtNIT.setText("");
            txtDireccion.setText("");
            txtSueldo.setText("");
            txtEstado.setText("");
            txtDepartamento.setText("");
            
            
            JOptionPane.showMessageDialog(null,"Registro del empleado existoso");
        }catch (Exception e){
            System.out.println("Error "+e);
        }
    }//GEN-LAST:event_RegistroEmpleadosActionPerformed

    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/empleados", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from empleado where emp_codigo = ?");
            pst.setString(1, txtbuscar.getText().trim());

            ResultSet rs = pst.executeQuery();

            if(rs.next()){
                txtEmpleado.setText(rs.getString("nombre_emp"));
                txtNIT.setText(rs.getString("NIT_emp"));
               txtDireccion.setText(rs.getString("direccion_emp"));
               txtSueldo.setText(rs.getString("sueldo_nominal"));
               txtEstado.setText(rs.getString("empStatus"));
               txtDepartamento.setText(rs.getString("departamento_depCodigo"));       
            } else {
                JOptionPane.showMessageDialog(null, "Empleado no registrado.");
            }

        }catch (Exception e){
        }

    }//GEN-LAST:event_BuscarActionPerformed

