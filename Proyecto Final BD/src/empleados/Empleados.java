    private void btnModificarEmpleadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarEmpleadosActionPerformed

       if(txtbuscar.getText().equals("")){

            JOptionPane.showMessageDialog(null,"Ingrese el nombre en el campo buscar");
        }

        if(txtEmpleado.getText().equals("")){

            JOptionPane.showMessageDialog(null,"Ingrese empleado");
        }
        try {
            String id = txtbuscar.getText().trim();
            
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/empleados", "root", "");
            PreparedStatement pst = cn.prepareStatement("update empleado set  nombre_emp = ?,NIT_emp = ?, direccion_emp= ?, sueldo_nominal= ?, empStatus=? , departamento_depCodigo=? where  emp_codigo = " + id);

              
            pst.setString(1, txtEmpleado.getText().trim());
            pst.setString(2, txtNIT.getText().trim());
            pst.setString(3, txtDireccion.getText().trim());
            pst.setString(4, txtSueldo.getText().trim());
            pst.setString(5, txtEstado.getText().trim());
            pst.setString(6, txtDepartamento.getText().trim());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Modificacion existosa");

            txtEmpleado.setText("");
            txtNIT.setText("");
            txtDireccion.setText("");
             txtSueldo.setText("");
             txtEstado.setText("");
            txtbuscar.setText("");
             txtDepartamento.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error"+e);
        }
    }//GEN-LAST:event_btnModificarEmpleadosActionPerformed

